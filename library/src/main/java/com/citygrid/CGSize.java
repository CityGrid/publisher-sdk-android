package com.citygrid;

public class CGSize {
    private float height;
    private float width;

    public CGSize(float height, float width) {
        this.height = height;
        this.width = width;
    }

    public float getHeight() {
        return height;
    }

    public float getWidth() {
        return width;
    }
}
